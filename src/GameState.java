/**
 * @author vishaalkumar
 * Enumeration for the current state of the game
 * Running - Game is running
 * Start - Game is on the start page
 * Won - User has won the game and displaying win screen
 * Lost - User has lost the game and displaying game over screen
 * Instructions - User is viewing the instructions page
 */
public enum GameState {
    RUNNING, START, WON, LOST, INSTRUCTIONS
}
